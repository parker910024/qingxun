#pragma mark - private method
    
    
-(UIImage *)getSubImage:(CGRect)rect {
    
    CGRect scaleRect = CGRectMake(rect.origin.x,rect.origin.y,rect.size.width * self.scale,rect.size.height * self.scale);
    
    CGImageRef subImageRef = CGImageCreateWithImageInRect(self.CGImage,scaleRect);
    CGRect smallBounds = CGRectMake(0, 0, CGImageGetWidth(subImageRef),CGImageGetHeight(subImageRef));
    UIGraphicsBeginImageContextWithOptions(smallBounds.size, NO, [UIScreen mainScreen].scale);
    CGContextRef context = UIGraphicsGetCurrentContext();
    CGContextDrawImage(context, smallBounds, subImageRef);
    UIImage* smallImage = [UIImage imageWithCGImage:subImageRef];
    UIGraphicsEndImageContext();
    
    return smallImage;
}
    
    
- (UIImage*) originImage:(UIImage *)image scaleToSize:(CGSize)size {
    
    UIGraphicsBeginImageContextWithOptions(size, NO, [UIScreen mainScreen].scale);
    
    [image drawInRect:CGRectMake(0, 0, size.width, size.height)];
    
    UIImage* scaledImage = UIGraphicsGetImageFromCurrentImageContext();
    
    UIGraphicsEndImageContext();
    
    return scaledImage;
}
    

